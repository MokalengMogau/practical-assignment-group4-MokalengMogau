public static void captureSeries(Scanner scanner) {
    if (seriesCount >= seriesArray.length) {
    System.out.println("Memory is full. Cannot add more series.");
    return;
    }
        
    System.out.println("CAPTURE A NEW SERIES");
    System.out.println("");

    System.out.print("Enter the series id: ");
    int id = Integer.parseInt(scanner.nextLine());
    System.out.print("Enter the series name: ");
    String name = scanner.nextLine();
        
    int ageRestriction = getValidAgeRestriction(scanner);
        
    System.out.print("Enter the number of episodes for " + name + ": ");
    int episodes = Integer.parseInt(scanner.nextLine());

    seriesArray[seriesCount] = new TVSeries(id, name, ageRestriction, episodes);
    seriesCount++;
    System.out.println("Series processed successfully!!!");
    }
    
    
private static int getValidAgeRestriction(Scanner scanner) {
    int age = -1;
    
while (true) {
    System.out.print("Enter the series age restriction: ");
    String input = scanner.nextLine();
    
try {
    age = Integer.parseInt(input);
if (age >= 2 && age <= 18) {
    return age;
    } else {
    System.out.println("You have entered an incorrect series age!");
    System.out.println("Please re-enter the series age >>");
    }
    } catch (NumberFormatException e) {
    System.out.println("You have entered an incorrect series age!");
    System.out.println("Please re-enter the series age >>");
     }
   }
}

public static TVSeries searchSeries(Scanner scanner) {
    System.out.print("Enter the series id to search: ");
    int id = Integer.parseInt(scanner.nextLine());

    for (int i = 0; i < seriesCount; i++) {
if (seriesArray[i].getSeriesId() == id) {
    System.out.println("----------------------------------------");
    System.out.println("SERIES ID: " + seriesArray[i].getSeriesId());
    System.out.println("SERIES NAME: " + seriesArray[i].getSeriesName());
                System.out.println("SERIES AGE RESTRICTION: " + seriesArray[i].getAgeRestriction());
                System.out.println("SERIES NUMBER OF EPISODES: " + seriesArray[i].getNumberOfEpisodes());
                return seriesArray[i]; // Return the found series object
            }
        }
        System.out.println("Series with Series Id: " + id + " was not found!");
        return null; // Return null if not found
    }

public static void updateSeries(Scanner scanner) {
    System.out.print("Enter the series id to update: ");
    int id = Integer.parseInt(scanner.nextLine());

    for (int i = 0; i < seriesCount; i++) {
if (seriesArray[i].getSeriesId() == id) {
    System.out.print("Enter the series name: ");
                
    String newName = scanner.nextLine();
                
    System.out.print("Enter the new age restriction: ");
    int newAge = getValidAgeRestriction(scanner);
    seriesArray[i].setAgeRestriction(newAge);

    System.out.print("Enter the new number of episodes: ");
    int newEpisodes = Integer.parseInt(scanner.nextLine());
    seriesArray[i].setNumberOfEpisodes(newEpisodes);
                
    System.out.println("Series with ID " + id + " has been updated.");
    return;
        }
    }
    System.out.println("Series with Series Id: " + id + " was not found!");
}

public static void deleteSeries(Scanner scanner) {
    System.out.print("Enter the series id to delete: ");
    int id = Integer.parseInt(scanner.nextLine());

    for (int i = 0; i < seriesCount; i++) {
if (seriesArray[i].getSeriesId() == id) {
    System.out.print("Are you sure you want to delete series " + id + " from the system? Yes (y) to delete. ");
   String confirmation = scanner.nextLine();
if (confirmation.equalsIgnoreCase("y")) {
                   
    for (int j = i; j < seriesCount - 1; j++) {seriesArray[j] = seriesArray[j + 1];
    }
    seriesArray[seriesCount - 1] = null; 
    seriesCount--;
    System.out.println("Series with Series Id: " + id + " WAS deleted!");
    }
    return;
        }
    }
    System.out.println("Series with Series Id: " + id + " was not found!");
}

public static void generateReport() {
    System.out.println("Series Report - 2025");
    System.out.println("----------------------------------------");
    for (int i = 0; i < seriesCount; i++) {
    System.out.println("Series " + (i + 1));
    System.out.println("----------------------------------------");
    System.out.println("SERIES ID: " + seriesArray[i].getSeriesId());
    System.out.println("SERIES NAME: " + seriesArray[i].getSeriesName());
    System.out.println("SERIES AGE RESTRICTION: " + seriesArray[i].getAgeRestriction());
    System.out.println("NUMBER OF EPISODES: " + seriesArray[i].getNumberOfEpisodes());
    System.out.println("----------------------------------------");
    }
    System.out.println("Report generated successfully!");
}

